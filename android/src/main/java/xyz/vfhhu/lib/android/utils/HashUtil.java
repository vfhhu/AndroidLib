    public static String getUniquePsuedoID() {
        String serial = null;

        String m_szDevIDShort = "35" +
                Build.BOARD.length() % 10 + Build.BRAND.length() % 10 +
                Build.CPU_ABI.length() % 10 + Build.DEVICE.length() % 10 +
                Build.DISPLAY.length() % 10 + Build.HOST.length() % 10 +
                Build.ID.length() % 10 + Build.MANUFACTURER.length() % 10 +
                Build.MODEL.length() % 10 + Build.PRODUCT.length() % 10 +
                Build.TAGS.length() % 10 + Build.TYPE.length() % 10 +
                Build.USER.length() % 10; //13 位

        try {
            serial = Build.class.getField("SERIAL").get(null).toString();
            //API>=9 使用serial号
            return new UUID(m_szDevIDShort.hashCode(), serial.hashCode()).toString().toUpperCase();
        } catch (Exception exception) {
            //serial需要一个初始化
            serial = "serial"; // 随便一个初始化
        }
        //使用硬件信息拼凑出来的15位号码
        return new UUID(m_szDevIDShort.hashCode(), serial.hashCode()).toString().toUpperCase();

    }
    public static String byte2hex(byte[] b,String split,boolean isUpperCase){ //二行制转字符串
        String hs="";
        String stmp="";
        for (int n=0;n<b.length;n++){
            stmp=(Integer.toHexString(b[n] & 0XFF));
            if(isUpperCase)stmp=stmp.toUpperCase();
            if (stmp.length()==1) hs=hs+"0"+stmp;
            else hs=hs+stmp;
            if (n<b.length-1)  hs=hs+split;
        }
        return hs;
    }
    public static String byte2hex(byte[] b){
        return byte2hex(b,"",false);
    }
    public static byte[] hex2byte(String s) {
        s=hexStringFormate(s);
        int len = s.length();
        byte[] data = new byte[len / 2];
        for (int i = 0; i < len; i += 2) {
            data[i / 2] = (byte) ((Character.digit(s.charAt(i), 16) << 4)
                    + Character.digit(s.charAt(i+1), 16));
        }
        return data;
    }
    public static String hexStringFormate(String s){
        return s.replaceAll("[^0-9A-Fa-f]", "");
    }
    public static String getHash(String s){
        String ret="";
        try {
            MessageDigest digest = MessageDigest.getInstance("SHA-256");
            byte[] hash = new byte[0];
            if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.KITKAT) {
                hash = digest.digest(s.getBytes(StandardCharsets.UTF_8));
            }else {
                hash = digest.digest(s.getBytes("UTF-8"));
            }
            ret=byte2hex(hash);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return ret;
    }
    public static String getCRC32(String s){
        CRC32 crc =new CRC32();
        crc.update(s.getBytes());
        return Long.toHexString(crc.getValue());


    }
